def syllables(word):
    return word[0], word[-1]

def main():
    seen = set()
    words = []
    for cat, lst in CATEGORIES.items():
        for w in lst:
            if w in seen:
                continue
            seen.add(w)
            s, e = syllables(w)
            words.append({
                "word": w,
                "category": cat,
                "start": s,
                "end": e,
                "length": len(w),
            })

    # 막다른 음절 분석: 끝 음절이지만 그 음절로 시작하는 단어가 없는 경우
    starts = collections.Counter(x["start"] for x in words)
    dead_ends = []
    for x in words:
        if starts.get(x["end"], 0) == 0:
            dead_ends.append(x["word"])

    # 끝 음절별 분포 (시작 단어 부족 음절 파악용)
    by_end = collections.Counter(x["end"] for x in words)

    data = {
        "meta": {
            "title": "유아~초등 2학년 수준 끝말잇기용 명사 데이터",
            "level": "유아 ~ 초등학교 2학년",
            "pos": "명사",
            "total": len(words),
            "category_counts": {c: sum(1 for x in words if x["category"] == c) for c in CATEGORIES},
            "dead_end_count": len(dead_ends),
            "dead_end_words": sorted(dead_ends),
        },
        "words": words,
    }

    with open("kids_nouns.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # 콘솔 리포트
    print("총 단어 수:", len(words))
    print("\n카테고리별 개수:")
    for c, n in data["meta"]["category_counts"].items():
        print(f"  {c}: {n}")
    print("\n막다른 음절(받을 단어 없음) 개수:", len(dead_ends))
    print("막다른 단어:", ", ".join(sorted(dead_ends)))
    print("\n시작 단어가 1개뿐인(거의 막다른) 끝음절:")
    risky = [(x["end"], starts.get(x["end"],0)) for x in words]
    cnt = {}
    for e, n in risky:
        cnt[e] = n
    only1 = sorted([e for e,n in cnt.items() if n == 1])
    print(", ".join(only1))

if __name__ == "__main__":
    main()
